	function saldo(){
		if (typeof $('#diario_insumo').val() == "undefined"){
    		var insumo = $("#insumo_e").val()
		}else{

			var insumo = $("#diario_insumo").val()
		}

		
		if (insumo != "") {
		$("#div_saldo").html('<div class="text-center"><div class="loadingsm"></div></div>');
		$("#div_saldo").load("class/parte_diario/saldo_insumo.php", {insumo: insumo});			
		}else{
			$("#div_saldo").html('');
		}
	}	
	
	function calculo_horas_tractor(){

		var h_inicial = $('#diario_hora_i').val()
		var h_final = $('#diario_hora_f').val()
		var horas = h_final - h_inicial

		if(horas > 0){
	
			$('#diario_hora_t').val(horas)

		}else{

			$('#diario_hora_t').val('')
			// $('#riego_inicial').val('')
			$('#diario_hora_f').val('')

		}
	}
	
