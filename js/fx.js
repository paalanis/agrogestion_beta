	function carga_diario(){

		if($('#diario_tractor').val() == 0 && $('#diario_hora_t').val() != 0){

		 $('#div_mensaje_general').html('<div id="mensaje_general" class="alert alert-danger alert-dismissible" style="height:47px" role="alert"><button type="button" class="close" data-dismiss="alert" aria-label="Close"><span aria-hidden="true">&times;</span></button>Seleccione tractor!</div>');

		}else if ($('#diario_tractor').val() != 0 && $('#diario_hora_t').val() == 0) {

		 $('#div_mensaje_general').html('<div id="mensaje_general" class="alert alert-danger alert-dismissible" style="height:47px" role="alert"><button type="button" class="close" data-dismiss="alert" aria-label="Close"><span aria-hidden="true">&times;</span></button>Faltan horas tractor!</div>');

		}else{
		
		if($('#control_cuartel').val() == 1){

		var id_cuartel_incial = 1
		var id_cuartel_final = $("#idfinal").val()
		var cuarteles = Array();
		var has = Array();
		for (var i = id_cuartel_incial; i <= id_cuartel_final; i++) {
			cuarteles.push("cuartel"+[i])
			has.push("has_seleccionadas"+[i])
		};
		var	s1 = $("#diario_fecha");  
		var campos = Array("diario_finca", "diario_tractor", "diario_hora_t", "diario_implemento", "diario_personal", "diario_horas", "diario_obs_general", "idinicial", "idfinal", "diario_labor", "diario_obs_labor", "totalhas");
		var campos = campos.concat(cuarteles);
		var campos = campos.concat(has);
		var campospasan = Array("diario_finca", "diario_tractor", "diario_hora_t", "diario_implemento", "diario_personal", "diario_horas", "diario_obs_general", "idinicial", "idfinal", "diario_labor", "diario_obs_labor", "totalhas");
		var campospasan = campospasan.concat(cuarteles);
		var campospasan = campospasan.concat(has);
		var pars = "diario_fecha=" + s1.val() + "&";
		for (i = 0; i < campos.length; i++) {
			campo = document.getElementById(campos[i]);
			pars = pars + campospasan[i] + "=" + campo.value + "&";
		}
			// alert(pars);
					$("#div_mensaje_general").html('<div class="text-center"><div class="loadingsm"></div></div>');
					$('#boton_guardar').attr('disabled', true);
					$.ajax({
							url : "class/parte_diario/parte_diario_bis.php",
							data : pars,
							dataType : "json",
							type : "get",

							success: function(data){
									
								if (data.success == 'true') {

									$('#div_mensaje_general').html('<div id="mensaje_general" class="alert alert-info alert-dismissible" style="height:47px" role="alert"><button type="button" class="close" data-dismiss="alert" aria-label="Close"><span aria-hidden="true">&times;</span></button>Ingreso exitoso!</div>');				
									
									setTimeout("llama_parte_diario()", 1050);
								} else {
									$('#div_mensaje_general').html('<div id="mensaje_general" class="alert alert-danger alert-dismissible" style="height:47px" role="alert"><button type="button" class="close" data-dismiss="alert" aria-label="Close"><span aria-hidden="true">&times;</span></button>Error reintente!</div>');				
									setTimeout("$('#mensaje_general').alert('close')", 2000);
								}
							
							}

					});
				}else{

					alert('Debe para parametrizar cuarteles')

				}	
				
		}

	}

	function carga_insumo(){

		document.getElementById('boton_insumo').value ='';
		document.getElementById('boton_insumo').disabled =true;

		var tamano_cuadro = $("#tamano_cuadro").val();

		var insumo = $("#diario_insumo").val()
		var cantidad = $("#diario_insumo_cantidad").val()
		var fecha = $("#diario_fecha").val()
		if (insumo == '' || cantidad == '' || fecha == '') {
	 		if (insumo == '') {
	 			$("#diario_insumo").tooltip({title: "Debe seleccionar", placement: "top"});
                $("#diario_insumo").tooltip('show');
	 		};
	 		if (cantidad == '') {
	 			$("#diario_insumo_cantidad").tooltip({title: "No debe ser cero", placement: "top"});
                $("#diario_insumo_cantidad").tooltip('show');
	 		};
	 		if (fecha == '') {
	 			$("#diario_fecha").tooltip({title: "Debe seleccionar", placement: "top"});
                $("#diario_fecha").tooltip('show');
	 		};
	 		document.getElementById('boton_insumo').disabled =false;
	 	}else{
		var pars = "diario_insumo=" + insumo + "&" + "diario_insumo_cantidad=" + cantidad + "&" + "diario_fecha=" + fecha + "&";
		// alert(pars);
					$("#div_insumos_cargados").html('<div class="text-center"><div class="loadingsm"></div></div>');
					$.ajax({
							url : "class/parte_diario/insumos.php",
							data : pars,
							dataType : "json",
							type : "get",

							success: function(data){
									
								if (data.success == 'true') {

									$("#diario_insumo").val('');
									$("#diario_insumo_cantidad").val('');
									document.getElementById('boton_insumo').disabled =false;
									$("#div_insumos_cargados").load("class/parte_diario/insumos_cargados.php", {tamano_cuadro: tamano_cuadro});
									$("#div_saldo").html('');

								} else {
									$('#div_insumos_cargados').html('<div id="mensaje_general" class="alert alert-danger alert-dismissible" style="height:47px" role="alert"><button type="button" class="close" data-dismiss="alert" aria-label="Close"><span aria-hidden="true">&times;</span></button>Error reintente!</div>');				
									setTimeout("$('#mensaje_general').alert('close')", 2000);
								}
							
							}

					});
					
		}
	}

